### operações com numeros
# +
# -
# *
# /
# %-> resto
# **-> exponencial

# +
valor1 = 10
valor2 = 15

soma = valor1 + valor2
print(soma)

# -
valor1 = 25
valor2 = 15

diferenca = valor1 - valor2
print(diferenca)

# *
valor1 = 5
valor2 = 300
produto = valor1 * valor2
print(produto)

# /
valor1 = 1000
valor2 = 25
quociente = valor1 / valor2
print(quociente)

# %
valor1 = 10
valor2 = 3
resto = valor1 % valor2
print(resto)

# **
valor1 = 10
valor2 = 11
potencia = valor1 ** valor2
print(potencia)
